import { execSync } from "child_process";
import fs from "fs";
import path from "path";

function updateFile<T>(filePath: string, key: string, value: string, type: "dotenv" | "js" = "dotenv") {
  let content = fs.existsSync(filePath) ? fs.readFileSync(filePath, "utf8") : "";
  let changed = false;

  if (type === "dotenv") {
    const lines = content.split(/\r?\n/);
    let found = false;

    const updatedLines = lines.map((line) => {
      if (line.startsWith(`${key}=`)) {
        found = true;
        if (line.substring(key.length + 1) !== value) {
          changed = true;
          return `${key}=${value}`;
        }
      }
      return line;
    });

    if (!found) {
      updatedLines.push(`${key}=${value}`);
      changed = true;
    }

    if (changed) {
      fs.writeFileSync(filePath, updatedLines.join("\n"));
    }
  } else if (type === "js") {
    let envObject: Record<string, string> = {};

    const match = content.match(/window\.__ENV\s*=\s*(\{[\s\S]*\});?/);
    if (match) {
      try {
        envObject = JSON.parse(match[1]);
      } catch {}
    }

    if (envObject[key] === value) {
      return;
    }

    envObject[key] = value;
    const newContent = "// This file is automatically generated.\n" + `window.__ENV = ${JSON.stringify(envObject, null, 2)};\n`;

    fs.writeFileSync(filePath, newContent);
  }
}

// 🔹 Hae git branch vain developmentissa
let branch = "unknown";
try {
  branch = execSync("git rev-parse --abbrev-ref HEAD").toString().trim();
} catch {
  console.warn("Git branch not available");
}

const root = process.cwd();
const VERSION_KEY = "VERSION";

updateFile(path.resolve(root, ".env.local"), VERSION_KEY, branch, "dotenv");
updateFile(path.resolve(root, "public/assets/__env.js"), VERSION_KEY, branch, "js");
